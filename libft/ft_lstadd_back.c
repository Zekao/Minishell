#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (alst)
	{
		tmp = ft_lstlast(*alst);
		if (!tmp)
			*alst = new;
		else
			tmp->next = new;
	}
}
